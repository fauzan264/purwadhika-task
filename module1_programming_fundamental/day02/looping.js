// 1. While Loop
/*
    InitialCondition;
    Condition;
    Exitway;

    Syntax:

    while(condition) {
        task;
    }
 */
let start = 1;
while(start <= 10) {
    console.log("Purwadhika");
    start++
}

let i = 5;
while(i <= 5) {
    console.log('Hello');
    i += 1 ;
}

let j = 3;
while(j <= 9) {
    console.log("Hai");
    j += 3;
}

// 2. Do-While
/*
    Syntax:

    do{
        task;
    } while (condition)
 */
let startLoop = 1;
do{
    console.log("Purwadhika");
    startLoop++;
} while (startLoop <= 10)

let startWhileLoop = 1;
while(startWhileLoop < 1 ) {
    console.log("while loop")
    startWhileLoop+=3
}

let startDoWhile = 1;
do{
    console.log("Do While")
    startDoWhile +=3
} while(startDoWhile < 1)

// 3. FOR LOOP (Mirip seperti while)
/*
    Syntax:
    for(initialValue; conditional; exitWay) {
        task;
    }
 */
for (let i = 1; i<= 10; i++) {
    console.log('Purwadhika')
}

for (let j=100; j<=103; j+=3) {
    console.log("Web Development")
}

